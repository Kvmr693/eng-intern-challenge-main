user_input = input()


if user_input.isalnum() and not (user_input.startswith('0') or user_input.startswith('.')):

    braille_notation = ''
    for char in user_input:
        if ord(char) in range(97, 123):  
            braille_notation += ''.join(''.join(str(x) for x in row) for row in braille_dict_lowercase[char])
        elif ord(char) in range(65, 91): 
            
            braille_notation += ''.join(''.join(str(x) for x in capital_row) for capital_row in capital_sign_matrix)
            
            lowercase_char = char.lower()
            braille_notation += ''.join(''.join(str(x) for x in row) for row in braille_dict_lowercase[lowercase_char])
        elif ord(char) in range(48, 58):  
            braille_notation += ''.join(''.join(str(x) for x in number_row) for number_row in braille_numbers[int(char)])
        elif char in symbol_list:  
            braille_notation += ''.join(''.join(str(x) for x in symbol_row) for symbol_row in braille_punctuation[char])
    print(braille_notation)

